public class ProductTagLine {
	
	private long productId;
	private long tagId;
	
	
	public void setProductId(long vproductId) {
		this.productId = vproductId;
	}
	
	public void setTagId(long vtagId) {
		this.tagId = vtagId;
	}
	
	
	public long getProductId () {
		
		return productId;
	}

public long getTagId () {
		
		return tagId;
	}

}
