public class Tag 
{
	private int id;
	private String name;
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String vName)
	{
		this.name = vName;
	}

	public int getId() 
	{
		return id;
	}

	public void setId(int id) 
	{
		this.id = id;
	}

}
